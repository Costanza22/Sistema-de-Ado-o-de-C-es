package Sistema;
class Cao {
    String nome;
    String raca;
    String coleira;
    String racao;
    boolean vacinaPulgas;
    boolean vacinaVermes;
    boolean vacinaCarrapatos;

    public Cao(String nome, String raca, String coleira, String racao, boolean vacinaPulgas, boolean vacinaVermes, boolean vacinaCarrapatos) {
        this.nome = nome;
        this.raca = raca;
        this.coleira = coleira;
        this.racao = racao;
        this.vacinaPulgas = vacinaPulgas;
        this.vacinaVermes = vacinaVermes;
        this.vacinaCarrapatos = vacinaCarrapatos;
    }
}

